// ----------------------------------------------------------------------
fun main(args: Array<String>) {
    func4A()
}



// ----------------------------------------------------------------------
fun func4A() {
    outer@ for (i in 1..100) {
        for (j in 1..100) {
            if (i==5 && j==6)
                break@outer
        }
    }
}
